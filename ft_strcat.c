#include <string.h>
#include "libft.h"

char *ft_strcat(char *s1, const char *s2)
{
	size_t	i;
	size_t	j;

	j = 0;
	i = 0;
	while (s1[i] != '\0')
	{
		i++;
	}
	while ((s1[i] = s2[j]))
	{
		i++;
		j++;
	}
	s1[i] = '\0';
	return (s1);
}
